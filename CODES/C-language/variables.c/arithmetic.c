//Implement programs to perform arithmetic operations (addition, subtraction, multiplication, division) on variables.
#include<stdio.h>
int main()
{
    int a=24,b=6;
    printf("\nFor int\n");
    printf("Addition of a+b=%d\n",a+b);
    printf("Subtraction of a-b=%d\n",a-b);
    printf("Multiplication of a*b=%d\n",a*b);
    printf("Division of a/b=%d\n",a/b);
    float c=12.36,d=2.06;
    printf("\nFor Float\n");
    printf("Addition of c+d=%f\n",c+d);
    printf("Subtraction of c-d=%f\n",c-d);
    printf("Multiplication of c*d=%f\n",c*d);
    printf("Division of c/d=%f\n",c/d);
    return 0;
}